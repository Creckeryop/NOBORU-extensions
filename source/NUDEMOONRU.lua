local stringify = Stringify

local function downloadContent(link)
	return DownloadContent(
		{
			Link = link,
			Header1 = "referer: https://www.google.com/",
			Header2 = "cookie: fusion_user=323815.769acb94801026e2f219844529cf51d0"
		}
